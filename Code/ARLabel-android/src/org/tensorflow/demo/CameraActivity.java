  @Override
  public void onSensorChanged(SensorEvent event) {
    //y轴：平行手机指向上

//		方向传感器提供三个数据，分别为azimuth、pitch和roll。
//
//		azimuth：方位，返回水平时磁北极和Y轴的夹角，范围为0°至360°。
//		0°=北，90°=东，180°=南，270°=西。
//
//		pitch：x轴和水平面的夹角，范围为-180°至180°。
//		当z轴向y轴转动时，角度为正值。
//
//		roll：y轴和水平面的夹角，由于历史原因，范围为-90°至90°。
//		当x轴向z轴移动时，角度为正值。
    sensorAngle[0] = event.values[0];
    sensorAngle[1] = -event.values[1];
    sensorAngle[2] = event.values[2];
    LOGGER.d("Orientation X: (Yaw) " + event.values[0]);  //绕垂直于手机平面的轴旋转，
    LOGGER.d("Orientation Y: (Pitch) " + event.values[1]);
    LOGGER.d("Orientation Z: (Roll)" + event.values[2]);
  }

